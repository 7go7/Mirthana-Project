# Функция поиска ключа с наибольшим совпадением
def find_best_match(transcript, script_data):
    best_similarity = float('inf')
    best_key = None
    for key, value in script_data.items():
        distance = Levenshtein.distance(transcript, value)
        if distance < best_similarity:
            best_similarity = distance
            best_key = key
            
    # Если схожесть выше определенного порога, то считаем что совпадения нет
    threshold = 0.3 * len(transcript)
    if best_similarity > threshold:
        return "NONE"
    
    return best_key

